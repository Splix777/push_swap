#include "push_swap.h"

void	rotate(t_list **lst)
{
	t_list	*head;

	head = *lst;
	if (!(*lst) || !((*lst)->next))
		return ;
	*lst = head->next;
	head->next = NULL;
	ft_lstlast(*lst)->next = head;
}

void	ra(t_list **a)
{
	rotate(a);
	ft_printf("ra\n");
}

void	rb(t_list **b)
{
	rotate(b);
	ft_printf("rb\n");
}

void	rr(t_list **a, t_list **b)
{
	rotate(a);
	rotate(b);
	ft_printf("rr\n");
}
